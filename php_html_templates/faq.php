        <div class="question">
            <p><span class="point">1.</span>4営業日で注文し、10日に入稿すれば、何日に発送してくれますか？</p>
        </div>
        <div class="answer">
            <p>10日受付（ご注文・データ入稿・ご入金の３点(代引の場合はご注文・データ入稿の２点)の確認が完了した日）の場合、ご入稿データに問題がなければ14日夕方以降に弊社から発送させていただく予定となります。<br />
                オプションで加工を追加されている場合は追加で納期をいただく形になります。<br />
                ※14日が弊社休業日の場合は翌営業日に発送となります。<br />
                詳しくは下記ページをご確認くださいませ。</p>
        </div>
